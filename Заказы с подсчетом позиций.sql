SELECT HYBRIS.ORDERS.P_CODE, RTRIM(RTRIM(HYBRIS.ORDERS.CREATEDTS, '0123456789'), ','), RTRIM(RTRIM(HYBRIS.ORDERS.P_TIMESLOTDELIVERYDATE, '0123456789'), ','), RTRIM(RTRIM(P_TIMESLOTDELIVERYSTARTDATE, '0123456789'), ','), RTRIM(RTRIM(P_TIMESLOTDELIVERYDATEEND, '0123456789'), ','), COUNT(HYBRIS.ORDERENTRIES.P_PRODUCT), SUM(P_QUANTITY)

FROM HYBRIS.ORDERS 

JOIN HYBRIS.ORDERENTRIES ON HYBRIS.ORDERS.PK = HYBRIS.ORDERENTRIES.P_ORDER

WHERE HYBRIS.ORDERS.CREATEDTS BETWEEN '01.05.18 00:00:00,000000000' AND '20.08.18 23:59:59,999999999'

GROUP BY HYBRIS.ORDERS.P_CODE, HYBRIS.ORDERS.CREATEDTS, HYBRIS.ORDERS.P_TIMESLOTDELIVERYDATE, P_TIMESLOTDELIVERYSTARTDATE, P_TIMESLOTDELIVERYDATEEND

ORDER BY HYBRIS.ORDERS.CREATEDTS;